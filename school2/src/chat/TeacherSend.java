package chat;
public class TeacherSend{
	private String teacher_send;
	private String parent_recv;
	private String comment;
	private String date;
	public String getTeacher_send() {
		return teacher_send;
	}
	public void setTeacher_send(String teacher_send) {
		this.teacher_send = teacher_send;
	}
	public String getParent_recv() {
		return parent_recv;
	}
	public void setParent_recv(String parent_recv) {
		this.parent_recv = parent_recv;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
}
