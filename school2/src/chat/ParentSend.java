package chat;
public class ParentSend{
	private String parent_send;
	private String teacher_recv;
	private String comment;
	private String date;
	public String getParent_send() {
		return parent_send;
	}
	public void setParent_send(String parent_send) {
		this.parent_send = parent_send;
	}
	public String getTeacher_recv() {
		return teacher_recv;
	}
	public void setTeacher_recv(String teacher_recv) {
		this.teacher_recv = teacher_recv;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
}